package logica;

/**
 * Define las áreas de especialización que puede tener un {@link Ingeniero}
 * dentro de una escudería.
 */
public enum Especialidad {

    /** Especialista en la unidad de potencia y sus componentes. */
    MOTOR,
    
    /** Especialista en la gestión y rendimiento de los compuestos de neumáticos. */
    NEUMATICOS,
    
    /** Especialista en el diseño y aerodinámica del chasis. */
    CHASIS,
    
    /** Especialista en los sistemas eléctricos, software y telemetría. */
    ELECTRONICA,
}